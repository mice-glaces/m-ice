    # But since the locale key could match anywhere, we search for exactly "\n    %s: {\n" % locale
    # and then the matching closing brace. A simpler way is to find the next locale key and insert before it,
    # or just replace the `    },` corresponding to that locale with the new translations.
    
    # Generate the string of new translations for this locale
    lines = []
    for k, v in new_translations[locale].items():
        # Escape quotes
        val = v.replace('"', '\\"').replace('\n', '\\n')
        lines.append(f"        '{k}': \"{val}\",")
    
    insert_str = "\n".join(lines) + "\n"
    
    # We will find the next locale definition or the end of the translations object.
    # It's tricky to parse JS with regex, so let's do a simple string replace.
    # We know that at the end of each locale block, there is a `    },`
    # Let's find the `    en: {` and insert before it for `fr`?
    # No, it's safer to just insert at the end of the locale object.
    # We can split the file by locale.
    
    if locale == "fr":
        # Insert before `    en: {`
        new_content = new_content.replace("\n    en: {", "\n" + insert_str + "\n    en: {")
